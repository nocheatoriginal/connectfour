package de.hhn.it.devtools.apis.connectfour;

/**
 * Enum for the colors of the players and the board states.
 */
public enum ConnectFourColor {
  /**
   * Red player.
   */
  RED,
  /**
   * Yellow player.
   */
  YELLOW,
  /**
   * Empty cell.
   */
  EMPTY,
  /**
   * Special cell.
   */
  SPECIAL,
  /**
   * Transparent cell.
   */
  TRANSPARENT

}
